def txt_to_pbm(string, output='out.pbm'):
    tablica = [[""] for i in range(18)]
    szerokosc = 0
    for i in string:
        if i == " ":
            i = "space"
        if i == ",":
            i = "przecinek"
        if i == ".":
            i = "kropka"
        if i == "!":
            i = "wykrzyknik"
        if i == "+":
            i = "plus"
        if i == "-":
            i = "minus"
        if i == "ć":
            i = "c"
        if i == "ą":
            i = "a"
        if i == "ę":
            i = "e"
        if i == "ł":
            i = "l"
        if i == "ż":
            i = "z"
        if i == "ó":
            i = "o"
        tymczasowy = litery[i].split("\n")
        for x in range(2, len(tymczasowy) - 1):
            tablica[x][0] = tablica[x][0] + tymczasowy[x]
            if x == 2:
                szerokosc = int(tymczasowy[2].split(" ")[0]) + szerokosc
    szerokosc = str(szerokosc)
    stringi = f"""P1
# {string}
{szerokosc} 15"""
    for y in range(len(tablica)):
        if y > 2:
            stringi = stringi + "\n" + tablica[y][0]
    print(stringi)
    with open(f"{output}", 'w') as file:
        file.write(stringi)


